public class Item {
    int item;
    int value;
    int weight;

    public Item() {
        this.item = 0;
        this.value = 0;
        this.weight = 0;
    }

    public Item(int item, int value, int weight) {
        this.item = item;
        this.value = value;
        this.weight = weight;
    }

}
